package entity;

public class Class {
    private String coursename;
    private String courseno;
    private String classno;
    private String tearchername;
    private String tearcherno;
    private int students;

    public String getCoursename() {
        return coursename;
    }

    public void setCoursename(String coursename) {
        this.coursename = coursename;
    }

    public String getCourseno() {
        return courseno;
    }

    public void setCourseno(String courseno) {
        this.courseno = courseno;
    }

    public String getClassno() {
        return classno;
    }

    public void setClassno(String classno) {
        this.classno = classno;
    }

    public String getTearchername() {
        return tearchername;
    }

    public void setTearchername(String tearchername) {
        this.tearchername = tearchername;
    }

    public String getTearcherno() {
        return tearcherno;
    }

    public void setTearcherno(String tearcherno) {
        this.tearcherno = tearcherno;
    }

    public int getStudents() {
        return students;
    }

    public void setStudents(int students) {
        this.students = students;
    }

}
